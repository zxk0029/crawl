# -*- coding: utf-8 -*-

from scrapy import signals

import random
import json
import io
import os


class RandomUserAgentMiddleware(object):
    def __init__(self):
        path_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'fake_useragent_0.1.11.json')
        data = json.loads(io.open(path_dir, encoding='utf-8', mode='rt').read())
        self.data_randomize = list(data['randomize'].values())
        self.data_browsers = data['browsers']

    def random_ua(self):
        browser = random.choice(self.data_randomize)
        ua = random.choice(self.data_browsers[browser])
        return ua

    def process_request(self, request, spider):
        request.headers["User-Agent"] = self.random_ua()


